class Solution{
public:
    int minSteps(int D){////////////////////done with help
        // code here
           int target = abs(D);
        int step = 0;
        int sum = 0;
        while (sum < target) {
            step++;
            sum += step;
        }
        while ((sum - target) % 2 != 0) {
            step++;
            sum += step;
        }
        return step;
    }
};

