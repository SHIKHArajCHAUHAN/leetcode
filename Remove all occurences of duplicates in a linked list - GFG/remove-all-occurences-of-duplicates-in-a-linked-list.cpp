class Solution
{
    public:
    Node* removeAllDuplicates(struct Node* head)
    {
        //code here
        map<int,int>mp;
        Node*temp=head;
       while(temp!=NULL)
       {
         mp[temp->data]++;
         temp=temp->next;
       }
       Node *temp2=new Node(-1);
       temp=temp2;
       for(auto i:mp)
       {
           if(i.second==1)
          {  temp->next=new Node(i.first);
          temp=temp->next;
          }
       }
       if(temp!=temp2)
       {
           temp2=temp2->next;
       return temp2;
       }
       return NULL;
    }
};

